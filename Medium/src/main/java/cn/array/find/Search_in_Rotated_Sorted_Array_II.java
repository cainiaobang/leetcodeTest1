    public boolean search(int[] nums, int target) {
        if(nums.length==0) return false;
        int left=0,right=nums.length-1,middle=(left+right)/2;
        if(nums[middle]==target)return true;
        while(nums[left]==nums[right]){
            ++left;
        }
        while(left<right){
            middle=(left+right)/2;
            if(nums[middle]<=nums[right]) right=middle;
            else  left=middle+1;
        }
        System.out.println(left);
        int pos1=find(nums,0,left-1,target);
        if(pos1!=-1){
            return true;
        }else{
            int pos2=find(nums,left,nums.length-1,target);
            return pos2!=-1? true:false;
        }
    }


